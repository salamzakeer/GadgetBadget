package com.gb.entitiy;

public class Funder {
	private Integer fId;
	private String funderName;
	private String funderPnumber;
	private Integer projID;
	private Integer fundAmount;
	private String description;
	

	public Funder() {
		
	}


	public Integer getfId() {
		return fId;
	}


	public void setfId(Integer fId) {
		this.fId = fId;
	}


	public String getFunderName() {
		return funderName;
	}


	public void setFunderName(String funderName) {
		this.funderName = funderName;
	}


	public String getFunderPnumber() {
		return funderPnumber;
	}


	public void setFunderPnumber(String funderPnumber) {
		this.funderPnumber = funderPnumber;
	}


	public Integer getProjID() {
		return projID;
	}


	public void setProjID(Integer projID) {
		this.projID = projID;
	}


	public Integer getFundAmount() {
		return fundAmount;
	}


	public void setFundAmount(Integer fundAmount) {
		this.fundAmount = fundAmount;
	}


	public String getDescription() {
		return description;
	}


	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
}
